#include <unistd.h>

int	main(int argc, char **argv)
{
	char	*s;
	char	c;

	if (argc == 2)
	{
		s = argv[1];
		while (*s)
		{
			if ((*s >= 'a' && *s <= 'm') || (*s >= 'A' && *s <= 'M'))
				c = *s + 13;
			else if ((*s >= 'n' && *s <= 'z') || (*s >= 'N' && *s <= 'Z'))
				c = *s - 13;
			else
				c = *s;
			write(1, &c, 1);
			s++;
		}
	}
	write(1, "\n", 1);
	return (0);
}
